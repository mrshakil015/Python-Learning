#String
x = "Hello World"
print(x)

#Integer
x = 20
print(x)

#Floating Point
x = 20.5
print(x)  # float

#Complex
x = 1j
print(x)

#List
x = ["apple", "banana", "cherry"]
print(x)

#Tuple
x = ("apple", "banana", "cherry")
print(x)

#Range
x = range(6)
print(x)

#Dictionary
x = {"name" : "John", "age" : 36}
print(x)

#Set
x = {"apple", "banana", "cherry"}
print(x)

#Boolean
x = True
print(x)

#None Type
x = None
print(x)  # NoneType
